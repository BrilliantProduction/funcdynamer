namespace FunctionalExtentions.Collections.Sorting
{
    /// <summary>
    /// Enumeration to customize sorting order
    /// </summary>
    public enum SortDirection
    {
        /// <summary>
        /// From smallest to largest (default)
        /// </summary>
        Up,

        /// <summary>
        /// From largest to smallest
        /// </summary>
        Down
    }
}